    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $idhotels = $request->idh;
        $idvisits= $request->visits;
        $fecha= $request->fecha;

        foreach($idhotels as $key => $idh){
            $input['idh'] = $idh;
            $input['idu'] = Auth::user()->id;
            $input['fecha'] = $fecha[$key];
            $input['visits'] = $idvisits[$key];
            Visit::create($input);
        }

        return redirect()->route('home')->with('message', 'Well done!, You has  successfully created new visits.');
    }

